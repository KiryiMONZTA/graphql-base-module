declare(strict_types=1);

namespace OxidEsales\GraphQL\Base\Framework;

class UserData implements UserDataInterface
{
    /** @var string */
    private $userId;

    public function __construct(string $userId)
    {
        $this->userId = $userId;
    }

    public function getUserId(): string
    {
        return $this->userId;
    }
}
